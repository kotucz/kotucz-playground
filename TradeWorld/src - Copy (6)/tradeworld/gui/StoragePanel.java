    public void updateIt() {

        jLabelName.setText(storage.getName() + " (" + storage.getOwner().getName() + ")" + System.currentTimeMillis());

        jListPayload.removeAll();

        jListPayload.setModel(new javax.swing.AbstractListModel() {

            public int getSize() {
                return storage.getPayload().getNumberOfGoodsTypes();
            }

            public Object getElementAt(int i) {
                Goods goods = storage.getPayload().getGoodsAt(i);
                return (goods.getType().toString() + " " + goods.getQuantity() + " (" + goods.getOwner().getName() + ")");
            }
        });

    }
